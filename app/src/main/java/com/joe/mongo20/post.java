package com.joe.mongo20;

public class post {

    private String name;
    private String email;
    private int mobile;
    private String city;
    private String date;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getMobile() {
        return mobile;
    }

    public String getCity() {
        return city;
    }

    public String getDate() {
        return date;
    }
}
